public class Item {
    private int ID, qtd;
    
    public void setID(int ID){ 
        this.ID = ID; 
    }
     
    public void setQtd(int qtd){ 
        this.qtd = qtd; 
    }
    
    public int getID (){ 
        return ID;
    }
    
    public int getQtd (){ 
        return qtd;
    }
}
